package classe.desafios;

public class MinhaData {
    String nome;
    int dia;
    int mes;
    int ano;
    public MinhaData() {
    }

    public MinhaData(String nome, int dia, int mes, int ano) {
        this.nome = nome;
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }
}
